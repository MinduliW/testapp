function rho = Density_statm(h)
    if h >25e3
        T = -131.21 + 0.00299*h;
        p = 2.488*((T+273.1)/216.6)^(-11.388);
        rho = p/(0.2869*(T+273.1));
        
    elseif h> 11e3 && h < 25e3
        T = -56.46;
        p = 22.65*exp(1.73 - 0.000157*h);
        rho = p/(0.2869*(T+273.1));
    else
        T = 15.04 - 0.00649*h;
        p = 101.29*((T+273.1)/288.08)^5.256;
        rho = p/(0.2869*(T+273.1));
    end
    

end


